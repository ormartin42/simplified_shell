#include "libft.h"

t_list	*ft_lstnew(void *data)
{
	t_list	*lstnew;

	lstnew = (t_list *)malloc(sizeof(t_list));
	if (!lstnew)
		return (NULL);
	lstnew->data = data;
	lstnew->next = NULL;
	return (lstnew);
}
